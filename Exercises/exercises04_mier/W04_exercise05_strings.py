# Det finns en mängd sträng-metoder som kan användas för att
# göra saker med strängar, t.ex. 
#       - göra om en sträng till stora bokstäver: str.upper()
#       - göra om en sträng till små bokstäver: str.lower():
#
# Exempel på hur str.upper() och str.lower() kan användas:
#
s = "kanelbullar innehåller kanel"
print(s.upper())        # Skriver ut "KANELBULLAR INNEHÅLLER KANEL"

S ="KANELBULLAR INNEHÅLLER KANEL"
print(S.lower())        # Skriver ut "kanelbullar innehåller kanel"


